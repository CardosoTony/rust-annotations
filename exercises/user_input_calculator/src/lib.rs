pub fn sum(x: f64, y: f64) {
    let sum = x + y;

    println!("Sum {} + {} = {}", x, y, sum);
}

pub fn subtract(x: f64, y: f64) {
    let subtract = x - y;

    println!("Subtract {} - {} = {}", x, y, subtract);
}

pub fn multiply(x: f64, y: f64) {
    let multiply = x * y;

    println!("Multiply {} * {} = {}", x, y, multiply);
}

pub fn divide(x: f64, y: f64) {
    let divide = x / y;

    println!("Divide {} / {} = {}", x, y, divide);
}
